def up_letter(value):
    """
    функция возвращает строку из заглавных букв
    :param value: строка
    :return: строка из заглавных букв
    """
    return value.upper()


def up_first_letter(value):
    """
    функция возвращает строку с заглавными первыми
    буквами каждого слова
    :param value: строка
    :return: строка с заглавными первыми
    буквами каждого слова
    """
    return value.title()